def open_sheet(name):
    gc = gspread.service_account()
    return gc.open(name)


def get_or_create_worksheet(sheet, title):
    try:
        worksheet = sheet.worksheet(title)
    except:
        return sheet.add_worksheet(title=title, rows="100", cols="100")

    return worksheet


def add_to_cell(worksheet, cell, data):
    worksheet.update(f"{cell}", data)
